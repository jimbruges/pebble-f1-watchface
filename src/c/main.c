static int int_sqrt(int n) {
    if (n <= 0) return 0;
    int lo = 0, hi = (n < 46340) ? n : 46340;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        int sq = mid * mid;
        if (sq == n) return mid;
        if (sq < n) lo = mid + 1;
        else hi = mid - 1;
    }
    return hi;
}

