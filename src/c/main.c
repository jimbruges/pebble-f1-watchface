static void inbox_received_callback(DictionaryIterator *iter, void *context) {
    (void)context;
    bool changed = false;

    Tuple *t;

    t = dict_find(iter, MESSAGE_KEY_FOLLOW_CALENDAR);
    if (t) {
        // value may be 1/0 (int) from Clay checkbox
        s_follow_calendar = (t->value->int32 != 0);
        changed = true;
    }
    t = dict_find(iter, MESSAGE_KEY_SHOW_TIME);
    if (t) { s_show_time = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_DATE);
    if (t) { s_show_date = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_DATE_FORMAT);
    if (t) { s_date_format = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_DATE_FONT);
    if (t) { s_date_font = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_SECONDS);
    if (t) {
        bool ns = (t->value->int32 != 0);
        if (ns != s_show_seconds) {
            s_show_seconds = ns;
            if (ns) {
                // Initialize progress from current seconds angle
                time_t now = time(NULL);
                struct tm *tt = localtime(&now);
                if (tt) {
                    int s_deg = 270 + tt->tm_sec * 6;
                    s_sec_progress = ((s_deg % 360) + 360) % 360 / 360.0f;
                    s_prev_sec = tt->tm_sec;
                }
            }
            update_tick_subscription();
        }
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_BG_COLOR);
    if (t) {
        s_bg_hex = t->value->int32;
        s_bg_color = GColorFromHEX(s_bg_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_TRACK_COLOR);
    if (t) {
        s_track_hex = t->value->int32;
        s_track_color = GColorFromHEX(s_track_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_HOUR_COLOR);
    if (t) {
        s_hour_hex = t->value->int32;
        s_hour_color = GColorFromHEX(s_hour_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_MINUTE_COLOR);
    if (t) {
        s_minute_hex = t->value->int32;
        s_minute_color = GColorFromHEX(s_minute_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_SECOND_COLOR);
    if (t) {
        s_second_hex = t->value->int32;
        s_second_color = GColorFromHEX(s_second_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_TEXT_COLOR);
    if (t) {
        s_text_hex = t->value->int32;
        s_text_color = GColorFromHEX(s_text_hex);
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_SHOW_RACE_NAME);
    if (t) { s_track_name_mode = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_RACE_NAME_FONT);
    if (t) { s_race_name_font = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_RANDOM_RACE_COLOURS);
    if (t) { s_random_race_colours = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_BATTERY);
    if (t) { s_show_battery = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_INVERT);
    if (t) { s_invert = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_BATTERY_CAR);
    if (t) { s_show_battery_car = (t->value->int32 != 0); update_battery_car(); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_STEPS_CAR);
    if (t) { s_show_steps_car = (t->value->int32 != 0); update_steps_car(); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_SLEEP_CAR);
    if (t) { s_show_sleep_car = (t->value->int32 != 0); update_sleep_car(); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_BATTERY_CAR_COLOR);
    if (t) { s_battery_car_hex = t->value->int32; s_battery_car_color = GColorFromHEX(s_battery_car_hex); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_STEPS_CAR_COLOR);
    if (t) { s_steps_car_hex = t->value->int32; s_steps_car_color = GColorFromHEX(s_steps_car_hex); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SLEEP_CAR_COLOR);
    if (t) { s_sleep_car_hex = t->value->int32; s_sleep_car_color = GColorFromHEX(s_sleep_car_hex); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_TIME_FONT);
    if (t) { s_time_font = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_TIME_MODE);
    if (t) { s_time_mode = atoi(t->value->cstring); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_SHOW_ANALOG);
    if (t) { s_show_analog = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_RACE_ON_HOUR);
    if (t) { s_race_on_hour = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_VIBRATE_ON_HOUR);
    if (t) { s_vibrate_on_hour = (t->value->int32 != 0); changed = true; }

    t = dict_find(iter, MESSAGE_KEY_FIXED_TRACK);
    if (t && t->length > 0) {
        strncpy(s_fixed_track, t->value->cstring, sizeof(s_fixed_track) - 1);
        s_fixed_track[sizeof(s_fixed_track) - 1] = '\0';
        changed = true;
    }
    t = dict_find(iter, MESSAGE_KEY_NEXT_RACE_TRACK);
    if (t && t->length > 0) {
        strncpy(s_next_track, t->value->cstring, sizeof(s_next_track) - 1);
        s_next_track[sizeof(s_next_track) - 1] = '\0';
        changed = true;
    }
    t = dict_find(iter, MESSAGE_KEY_NEXT_RACE_NAME);
    if (t && t->length > 0) {
        strncpy(s_next_name, t->value->cstring, sizeof(s_next_name) - 1);
        s_next_name[sizeof(s_next_name) - 1] = '\0';
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_NEXT_RACE_COUNTRY);
    if (t && t->length > 0) {
        strncpy(s_next_country, t->value->cstring, sizeof(s_next_country) - 1);
        s_next_country[sizeof(s_next_country) - 1] = '\0';
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_RACE_INDICATOR);
    if (t && t->length > 0) {
        strncpy(s_race_indicator, t->value->cstring, sizeof(s_race_indicator) - 1);
        s_race_indicator[sizeof(s_race_indicator) - 1] = '\0';
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_SHOW_RACE_INDICATOR);
    if (t) {
        s_show_race_indicator = t->value->int32;
        changed = true;
    }

    t = dict_find(iter, MESSAGE_KEY_RACE_NOW);
    if (t && t->value->int32) {
        time_t now = time(NULL);
        struct tm *tm = localtime(&now);
        int laps = tm->tm_hour % 12;
        if (laps == 0) laps = 12;
        start_race(laps);
    }

    if (changed) {
        save_settings();

    reconfigure_time_mode();
        text_layer_set_font(s_race_layer, get_race_name_font());
        GRect b = layer_get_bounds(window_get_root_layer(s_window));
        layer_set_frame(text_layer_get_layer(s_race_layer), GRect(0, 4, b.size.w, get_race_name_font_height()));
        text_layer_set_text_color(s_race_layer, eff_text_color());
        text_layer_set_text_color(s_time_layer, eff_text_color());
        text_layer_set_text_color(s_date_layer, eff_text_color());
        text_layer_set_text_color(s_min_layer, eff_text_color());
        time_t now = time(NULL);
        struct tm *tm_now = localtime(&now);
        if (tm_now) update_texts(tm_now);
        layer_mark_dirty(s_canvas_layer);
    }
}

