// Draw the closed loop as a series of thick segments with circular joints,
// producing a smooth continuous slot-car style ribbon of asphalt.
static void draw_loop(GContext *ctx, GColor color, int width) {
    if (s_num_wp < 2) return;
    graphics_context_set_stroke_color(ctx, color);
    graphics_context_set_fill_color(ctx, color);
    graphics_context_set_stroke_width(ctx, width);
    for (uint32_t i = 0; i < s_num_wp; i++) {
        uint32_t j = (i + 1) % s_num_wp;
        graphics_draw_line(ctx, s_center[i], s_center[j]);
        // round the joint so corners don't have notches
        if (width >= 4) {
            graphics_fill_circle(ctx, s_center[i], width / 2);
        }
    }
}

// Wide kerb outline first (behind), then asphalt on top, then center slot.
static void draw_track_layered(GContext *ctx) {
    if (s_num_wp < 2) return;

    // Outer edge (kerb) — widest, light color
    draw_loop(ctx, KERB_COLOR, 14);
    // Asphalt surface on top — narrower, dark
    draw_loop(ctx, s_track_color, 10);
    // Center slot line — thin
    graphics_context_set_stroke_color(ctx, SLOT_COLOR);
    graphics_context_set_stroke_width(ctx, 1);
    for (uint32_t i = 0; i < s_num_wp; i++) {
        uint32_t j = (i + 1) % s_num_wp;
        graphics_draw_line(ctx, s_center[i], s_center[j]);
    }

    // Start/finish line: a small checkered bar across the track at wp[0]
    GPoint a = s_center[0];
    GPoint b = s_center[1 % s_num_wp];
    int dx = b.x - a.x;
    int dy = b.y - a.y;
    int len = int_sqrt(dx * dx + dy * dy);
    if (len > 0) {
        // perpendicular unit (scaled), length ~7px each side
        int px = -dy * 7 / len;
        int py =  dx * 7 / len;
        graphics_context_set_stroke_color(ctx, START_LINE);
        graphics_context_set_stroke_width(ctx, 3);
        graphics_draw_line(ctx, GPoint(a.x - px, a.y - py), GPoint(a.x + px, a.y + py));
    }
}

// 80s F1 car silhouette (top-down view), authored pointing in +X (east).
// Coordinates in a ~ -8..+8 local space. The body tapers from a narrow nose
// (front, +X) to a wider rear (-X) with front and rear wings.
//   nose tip -> front wing -> sidepods -> rear wing
static const GPathInfo F1_CAR_PATH = {
    .num_points = 12,
    .points = (GPoint[]) {
        { 9,  0},   // nose tip (front)
        { 5,  2},   // nose shoulder (left)
        { 4,  5},   // front wing left
        { 2,  2},   // body in
        {-4,  3},   // sidepod left / rear body
        {-6,  5},   // rear wing left
        {-7,  0},   // rear center
        {-6, -5},   // rear wing right
        {-4, -3},   // sidepod right
        {-2, -2},   // body in
        {-4, -5},   // front wing right
        {-5, -2},   // nose shoulder (right)
    }
};

// Rotate a local car point by the heading (cos/sin in TRIG_MAX_RATIO units),
// scale, and translate to screen position.
static GPoint car_xf(GPoint local, int32_t cos_a, int32_t sin_a, int scale_num, GPoint at) {
    // scale_num is in 1/4 units (4 = 1.0x). local coords are ~ -9..9.
    int lx = local.x * scale_num / 4;
    int ly = local.y * scale_num / 4;
    GPoint p;
    p.x = at.x + (int16_t)((lx * cos_a - ly * sin_a) / TRIG_MAX_RATIO);
    p.y = at.y + (int16_t)((lx * sin_a + ly * cos_a) / TRIG_MAX_RATIO);
    return p;
}

// Draw a small 80s F1 car at pos, oriented along (dir_x,dir_y), in `color`.
// `scale_num` is size in quarter units (5 ~= small, 6 ~= large).
static void draw_car(GContext *ctx, GPoint pos, int dir_x, int dir_y,
                     GColor color, int scale_num) {
    // Heading angle from direction vector.
    int32_t angle = atan2_lookup((int16_t)dir_y, (int16_t)dir_x);
    int32_t cos_a = cos_lookup(angle);
    int32_t sin_a = sin_lookup(angle);

    // Transform body outline.
    GPoint pts[12];
    for (int i = 0; i < 12; i++) {
        pts[i] = car_xf(F1_CAR_PATH.points[i], cos_a, sin_a, scale_num, pos);
    }
    GPathInfo info = { .num_points = 12, .points = pts };
    GPath *body = gpath_create(&info);
    if (!body) return;

    // Tyres: four black blocks at the corners (local coords), drawn first.
    graphics_context_set_fill_color(ctx, GColorBlack);
        const GPoint tyres[4] = { {-4, 5}, {-4, -5}, {5, 5}, {5, -5} };
    for (int i = 0; i < 4; i++) {
        GPoint c = car_xf(tyres[i], cos_a, sin_a, scale_num, pos);
        graphics_fill_circle(ctx, c, 2);
    }

    // Body fill + dark outline for contrast against the track.
    graphics_context_set_fill_color(ctx, color);
    gpath_draw_filled(ctx, body);
    graphics_context_set_stroke_color(ctx, GColorBlack);
    graphics_context_set_stroke_width(ctx, 1);
    gpath_draw_outline(ctx, body);

    // Cockpit dot (white) just behind the nose.
    GPoint cockpit = car_xf(GPoint(0, 0), cos_a, sin_a, scale_num, pos);
    graphics_context_set_fill_color(ctx, GColorWhite);
    graphics_fill_circle(ctx, cockpit, 1);

    gpath_destroy(body);
}

