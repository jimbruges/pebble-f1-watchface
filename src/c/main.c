static const char *get_date_str(struct tm *t) {
    static char buf[24];
    switch (s_date_format) {
        case 1:  strftime(buf, sizeof(buf), "%d %b", t); break;   // DD MMM
        case 2:  strftime(buf, sizeof(buf), "%m/%d", t); break;   // MM/DD
        case 3:  strftime(buf, sizeof(buf), "%b %d", t); break;   // MMM DD
        case 4:  strftime(buf, sizeof(buf), "%a %d", t); break;   // Day DD
        default: strftime(buf, sizeof(buf), "%d/%m", t); break;   // DD/MM
    }
    return buf;
}

// Strip trailing Roman numeral suffixes (e.g. "SILVERSTONE VII" -> "SILVERSTONE")
static char s_stripped_name[64];
static const char *strip_roman(const char *name) {
    strncpy(s_stripped_name, name, sizeof(s_stripped_name) - 1);
    s_stripped_name[sizeof(s_stripped_name) - 1] = '\0';
    // Walk backwards: Roman numerals are uppercase letters I, V, X, L, C, D, M
    int len = strlen(s_stripped_name);
    int i = len - 1;
    // Skip trailing spaces
    while (i >= 0 && s_stripped_name[i] == ' ') i--;
    // Check for Roman numeral suffix
    int end = i;
    while (i >= 0 && (s_stripped_name[i] == 'I' || s_stripped_name[i] == 'V' ||
                       s_stripped_name[i] == 'X' || s_stripped_name[i] == 'L' ||
                       s_stripped_name[i] == 'C' || s_stripped_name[i] == 'D' ||
                       s_stripped_name[i] == 'M')) {
        i--;
    }
    // Only strip if we found Roman chars and there's a space before them
    if (i >= 0 && i < end && s_stripped_name[i] == ' ') {
        s_stripped_name[i] = '\0';
    }
    return s_stripped_name;
}

