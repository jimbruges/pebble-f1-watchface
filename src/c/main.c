static int find_circuit(const char *key) {
    if (key == NULL || key[0] == '\0') return -1;
    for (int i = 0; i < NUM_CIRCUITS; i++) {
        if (strcmp(s_circuits[i].name, key) == 0) return i;
    }
    return -1;
}

