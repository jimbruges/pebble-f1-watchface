static void window_load(Window *window) {
    Layer *window_layer = window_get_root_layer(window);
    GRect bounds = layer_get_bounds(window_layer);

    s_canvas_layer = layer_create(bounds);
    layer_set_update_proc(s_canvas_layer, canvas_update_proc);
    layer_add_child(window_layer, s_canvas_layer);

    int race_h = get_race_name_font_height();
    s_race_layer = text_layer_create(GRect(0, 4, bounds.size.w, race_h));
    text_layer_set_text_color(s_race_layer, eff_text_color());
    text_layer_set_background_color(s_race_layer, GColorClear);
    text_layer_set_font(s_race_layer, get_race_name_font());
    text_layer_set_text_alignment(s_race_layer, GTextAlignmentCenter);
    layer_add_child(window_layer, text_layer_get_layer(s_race_layer));

    int time_y = (int)bounds.size.h - 52;
    s_time_layer = text_layer_create(GRect(0, time_y, bounds.size.w, 42));
    text_layer_set_text_color(s_time_layer, eff_text_color());
    text_layer_set_background_color(s_time_layer, GColorClear);
    text_layer_set_font(s_time_layer, fonts_get_system_font(FONT_KEY_BITHAM_30_BLACK));
    text_layer_set_text_alignment(s_time_layer, GTextAlignmentCenter);
    layer_add_child(window_layer, text_layer_get_layer(s_time_layer));

    int date_y = (int)bounds.size.h - 20;
    s_date_layer = text_layer_create(GRect(0, date_y, bounds.size.w, 20));
    text_layer_set_text_color(s_date_layer, eff_text_color());
    text_layer_set_background_color(s_date_layer, GColorClear);
    text_layer_set_font(s_date_layer, get_date_font());
    text_layer_set_text_alignment(s_date_layer, GTextAlignmentCenter);
    layer_add_child(window_layer, text_layer_get_layer(s_date_layer));

    s_min_layer = text_layer_create(GRect(0, 0, 0, 0));
    text_layer_set_text_color(s_min_layer, eff_text_color());
    text_layer_set_background_color(s_min_layer, GColorClear);
    text_layer_set_font(s_min_layer, fonts_get_system_font(FONT_KEY_BITHAM_30_BLACK));
    text_layer_set_text_alignment(s_min_layer, GTextAlignmentRight);
    layer_set_hidden(text_layer_get_layer(s_min_layer), true);
    layer_add_child(window_layer, text_layer_get_layer(s_min_layer));

    s_battery_layer = layer_create(GRect(bounds.size.w - 26, 6, 24, 5));
    layer_set_update_proc(s_battery_layer, battery_layer_update_proc);
    layer_add_child(window_layer, s_battery_layer);

    reconfigure_time_mode();

    time_t now = time(NULL);
    struct tm *current_time = localtime(&now);
    if (current_time) update_texts(current_time);
}

static void window_unload(Window *window) {
    layer_destroy(s_canvas_layer);
    text_layer_destroy(s_race_layer);
    text_layer_destroy(s_time_layer);
    text_layer_destroy(s_date_layer);
    text_layer_destroy(s_min_layer);
    layer_destroy(s_battery_layer);
}

static void init(void) {
    load_settings();

    s_window = window_create();
    window_set_window_handlers(s_window, (WindowHandlers){
        .load = window_load,
        .unload = window_unload
    });
    window_stack_push(s_window, true);

    update_tick_subscription();

    // Battery car
    battery_state_service_subscribe(battery_handler);
    update_battery_car();

#ifdef PBL_HEALTH
    // Steps / sleep cars
    health_service_events_subscribe(health_handler, NULL);
    update_steps_car();
    update_sleep_car();
#endif

    app_message_register_inbox_received(inbox_received_callback);
    app_message_open(512, 64);
}

static void deinit(void) {
    tick_timer_service_unsubscribe();
    battery_state_service_unsubscribe();
#ifdef PBL_HEALTH
    health_service_events_unsubscribe();
#endif
    window_destroy(s_window);
}

int main(void) {
    init();
    app_event_loop();
    deinit();
    return 0;
}
