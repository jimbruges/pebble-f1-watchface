static void reconfigure_time_mode(void) {
    if (!s_time_layer || !s_date_layer || !s_race_layer) return;
    GRect bounds = layer_get_bounds(window_get_root_layer(s_window));
    int w = bounds.size.w, h = bounds.size.h;
    GFont font = get_time_font();

    // LED mode always forces the Leco LED-style font
    if (s_time_mode == 2) {
        font = fonts_get_system_font(FONT_KEY_LECO_42_NUMBERS);
    }

    int date_h = get_date_font_height();
    int time_h = get_time_font_height();
    int row_h = date_h > time_h ? date_h : time_h;
    switch (s_time_mode) {
        case 1: // analog — hide text layers, draw hands on canvas
            layer_set_hidden(text_layer_get_layer(s_time_layer), true);
            layer_set_hidden(text_layer_get_layer(s_date_layer), true);
            break;
        case 3: // split corners — hours left, date middle, minutes right, all inline at bottom
            text_layer_set_font(s_time_layer, font);
            text_layer_set_text_alignment(s_time_layer, GTextAlignmentLeft);
            layer_set_frame(text_layer_get_layer(s_time_layer), GRect(4, h - row_h, w / 3 - 4, row_h));
            text_layer_set_font(s_date_layer, get_date_font());
            text_layer_set_text_alignment(s_date_layer, GTextAlignmentCenter);
            layer_set_frame(text_layer_get_layer(s_date_layer), GRect(w / 3, h - row_h, w / 3, row_h));
            text_layer_set_font(s_min_layer, font);
            text_layer_set_text_alignment(s_min_layer, GTextAlignmentRight);
            layer_set_frame(text_layer_get_layer(s_min_layer), GRect(2 * w / 3, h - row_h, w / 3 - 4, row_h));
            layer_set_hidden(text_layer_get_layer(s_time_layer), false);
            layer_set_hidden(text_layer_get_layer(s_min_layer), false);
            layer_set_hidden(text_layer_get_layer(s_date_layer), false);
            break;
        default: // 0=digital — standard bottom layout
            text_layer_set_font(s_time_layer, font);
            text_layer_set_text_alignment(s_time_layer, GTextAlignmentCenter);
            layer_set_frame(text_layer_get_layer(s_time_layer), GRect(0, h - row_h - date_h, w, row_h));
            text_layer_set_font(s_date_layer, get_date_font());
            text_layer_set_text_alignment(s_date_layer, GTextAlignmentCenter);
            layer_set_frame(text_layer_get_layer(s_date_layer), GRect(0, h - date_h, w, date_h));
            layer_set_hidden(text_layer_get_layer(s_time_layer), false);
            layer_set_hidden(text_layer_get_layer(s_date_layer), false);
            break;
    }
    apply_visibility();
    layer_mark_dirty(s_canvas_layer);
}

// Race animation: smooth redraw loop
#define RACE_FPS 45
static void race_timer_cb(void *data) {
    (void)data;
    if (!s_race_active) return;
    time_t now_s = 0;
    uint16_t now_ms_part = 0;
    time_ms(&now_s, &now_ms_part);
    int32_t elapsed_s = (int32_t)(now_s - s_race_start_s);
    int32_t elapsed_ms = elapsed_s * 1000 + (int32_t)now_ms_part - (int32_t)s_race_start_ms_part;
    int32_t duration_ms = s_race_laps * 3000;
    if (elapsed_ms >= duration_ms) {
        s_race_active = false;
        // Restore original colours
        if (s_random_race_colours) {
            s_bg_color = s_orig_bg_color;
            s_bg_hex = s_orig_bg_hex;
            s_text_color = s_orig_text_color;
            s_text_hex = s_orig_text_hex;
            text_layer_set_text_color(s_time_layer, eff_text_color());
            text_layer_set_text_color(s_date_layer, eff_text_color());
            text_layer_set_text_color(s_min_layer, eff_text_color());
            text_layer_set_text_color(s_race_layer, eff_text_color());
        }
        layer_mark_dirty(s_canvas_layer);
        return;
    }
    layer_mark_dirty(s_canvas_layer);
    s_race_timer = app_timer_register(1000 / RACE_FPS, race_timer_cb, NULL);
}

// Vibrant F1-style background colours for random race mode
static const uint32_t s_race_palette[] = {
    0x0055FF, // blue
    0xFF0000, // red
    0x00AA00, // green
    0xFF6600, // orange
    0x9900CC, // purple
    0x00CCCC, // teal
    0xCC0066, // magenta
    0x333333, // dark grey
    0x003366, // navy
    0x880000, // dark red
    0x006644, // forest
    0x663300, // brown
};
#define RACE_PALETTE_SIZE (sizeof(s_race_palette) / sizeof(s_race_palette[0]))

static uint32_t get_contrasting_text(uint32_t bg_hex) {
    // Compute luminance: 0.299*R + 0.587*G + 0.114*B
    int r = (bg_hex >> 16) & 0xFF;
    int g = (bg_hex >> 8) & 0xFF;
    int b = bg_hex & 0xFF;
    int lum = (299 * r + 587 * g + 114 * b) / 1000;
    return (lum > 128) ? 0x000000 : 0xFFFFFF;
}

static void start_race(int laps) {
    if (s_race_timer) {
        app_timer_cancel(s_race_timer);
        s_race_timer = NULL;
    }
    // Randomise colours if enabled
    if (s_random_race_colours) {
        s_orig_bg_hex = s_bg_hex;
        s_orig_bg_color = s_bg_color;
        s_orig_text_hex = s_text_hex;
        s_orig_text_color = s_text_color;
        // Simple hash from current time for pseudo-random index
        time_t now = time(NULL);
        uint32_t idx = (uint32_t)now;
        idx = (idx * 2654435761u) >> 16;  // Knuth multiplicative hash
        idx = idx % RACE_PALETTE_SIZE;
        s_bg_hex = s_race_palette[idx];
        s_bg_color = GColorFromHEX(s_bg_hex);
        uint32_t text_hex = get_contrasting_text(s_bg_hex);
        s_text_hex = text_hex;
        s_text_color = GColorFromHEX(text_hex);
        text_layer_set_text_color(s_time_layer, eff_text_color());
        text_layer_set_text_color(s_date_layer, eff_text_color());
        text_layer_set_text_color(s_min_layer, eff_text_color());
        text_layer_set_text_color(s_race_layer, eff_text_color());
    }
    // Ensure track geometry is ready
    s_active_circuit = resolve_active_circuit();
    compute_track();
    // Capture the hour car's current position as the race starting point
    GPoint center = { s_analog_cx, s_analog_cy };
    time_t now = time(NULL);
    struct tm *t = localtime(&now);
    // Same formula as canvas_update_proc: 270 + hour*30 + min/2
    int hour_angle_deg = 270 + ((t->tm_hour % 12) * 30) + (t->tm_min / 2);
    int32_t hour_trig = DEG_TO_TRIGANGLE(hour_angle_deg);
    s_race_start_progress = angle_to_track_progress(center, hour_trig);
    s_race_active = true;
    s_race_laps = laps;
    time_ms(&s_race_start_s, &s_race_start_ms_part);
    layer_mark_dirty(s_canvas_layer);
    s_race_timer = app_timer_register(1000 / RACE_FPS, race_timer_cb, NULL);
}

// ---- Battery / Health cars ------------------------------------------------

static void update_battery_car(void) {
    BatteryChargeState bat = battery_state_service_peek();
    // 100% at start line → 0% at finish (clockwise)
    s_battery_progress = (100.0f - (float)bat.charge_percent) / 100.0f;
}

#ifdef PBL_HEALTH
static void update_steps_car(void) {
    // Use explicit time range from midnight to now
    time_t start = time_start_of_today();
    time_t end = time(NULL);
    int32_t steps = health_service_sum(HealthMetricStepCount, start, end);
    if (steps < 0) steps = 0;
    uint32_t goal = 10000;
    s_steps_progress = (float)steps / (float)goal;
    if (s_steps_progress > 1.0f) s_steps_progress = 1.0f;
    s_steps_goal_exceeded = ((uint32_t)steps >= goal);
}

static void update_sleep_car(void) {
    time_t start = time_start_of_today();
    time_t end = time(NULL);
    int32_t sleep_secs = health_service_sum(HealthMetricSleepSeconds, start, end);
    if (sleep_secs < 0) sleep_secs = 0;
    uint32_t goal = 8 * 3600;
    s_sleep_progress = (float)sleep_secs / (float)goal;
    if (s_sleep_progress > 1.0f) s_sleep_progress = 1.0f;
}

static void health_handler(HealthEventType event, void *context) {
    if (event == HealthEventSignificantUpdate) {
        update_steps_car();
        update_sleep_car();
        layer_mark_dirty(s_canvas_layer);
    }
}
#endif

static void battery_handler(BatteryChargeState state) {
    update_battery_car();
    layer_mark_dirty(s_canvas_layer);
}

static void tick_handler(struct tm *tick_time, TimeUnits units_changed) {
    if (tick_time == NULL) return;

    // Detect hour change → start race animation
    if (s_race_on_hour && (units_changed & HOUR_UNIT)) {
        int laps = tick_time->tm_hour % 12;
        if (laps == 0) laps = 12;
        start_race(laps);
        if (s_vibrate_on_hour) {
            vibes_short_pulse();
        }
    }

    // Only refresh the digital text on minute boundaries to save a little work.
    if ((units_changed & MINUTE_UNIT) || s_time_buf[0] == '\0') {
        update_texts(tick_time);
    }
    // Update extra cars at same frequency as the clock
    update_battery_car();
#ifdef PBL_HEALTH
    update_steps_car();
    update_sleep_car();
#endif
    layer_mark_dirty(s_canvas_layer);
}

// Subscribe to the tick service at the resolution the seconds car needs.
// SECOND_UNIT is used ONLY when the user enables the seconds car; otherwise
// MINUTE_UNIT keeps the watchface battery-efficient.
static void update_tick_subscription(void) {
    tick_timer_service_unsubscribe();
    TimeUnits units = MINUTE_UNIT;
    if (s_show_seconds) units = SECOND_UNIT;
    if (s_race_on_hour) units |= HOUR_UNIT;
    tick_timer_service_subscribe(units, tick_handler);
}

